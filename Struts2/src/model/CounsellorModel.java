package model;

public class CounsellorModel {
	private int counsellorID;//辅导员编号
	private String counsellorName;//辅导员姓名
	private int counsellorSex;//性别
	private String belongToCollege;//所属院系
	private String belongToSpecialty;//所属专业
	private String takeClass;//所带班级
	private String counsellorAddress;//住址
	private String counsellorIphone;//联系方式
	public int getCounsellorID() {
		return counsellorID;
	}
	public void setCounsellorID(int counsellorID) {
		this.counsellorID = counsellorID;
	}
	public String getCounsellorName() {
		return counsellorName;
	}
	public void setCounsellorName(String counsellorName) {
		this.counsellorName = counsellorName;
	}
	public int getCounsellorSex() {
		return counsellorSex;
	}
	public void setCounsellorSex(int counsellorSex) {
		this.counsellorSex = counsellorSex;
	}
	public String getBelongToCollege() {
		return belongToCollege;
	}
	public void setBelongToCollege(String belongToCollege) {
		this.belongToCollege = belongToCollege;
	}
	public String getBelongToSpecialty() {
		return belongToSpecialty;
	}
	public void setBelongToSpecialty(String belongToSpecialty) {
		this.belongToSpecialty = belongToSpecialty;
	}
	public String getTakeClass() {
		return takeClass;
	}
	public void setTakeClass(String takeClass) {
		this.takeClass = takeClass;
	}
	public String getCounsellorAddress() {
		return counsellorAddress;
	}
	public void setCounsellorAddress(String counsellorAddress) {
		this.counsellorAddress = counsellorAddress;
	}
	public String getCounsellorIphone() {
		return counsellorIphone;
	}
	public void setCounsellorIphone(String counsellorIphone) {
		this.counsellorIphone = counsellorIphone;
	}
	

}
