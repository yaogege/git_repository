package model;

public class CollegeModel {
	
	private int collegeID;//学院ID
    private String collegeName;//学院名字
    private int collegeNumber;//学院人数
    private String collegeSummarize;//学院简介
    
	public int getCollegeID() {
		return collegeID;
	}
	public void setCollegeID(int collegeID) {
		this.collegeID = collegeID;
	}
	public String getCollegeName() {
		return collegeName;
	}
	public void setCollegeName(String collegeName) {
		this.collegeName = collegeName;
	}
	public int getCollegeNumber() {
		return collegeNumber;
	}
	public void setCollegeNumber(int collegeNumber) {
		this.collegeNumber = collegeNumber;
	}
	public String getCollegeSummarize() {
		return collegeSummarize;
	}
	public void setCollegeSummarize(String collegeSummarize) {
		this.collegeSummarize = collegeSummarize;
	}
    
    
    

}
